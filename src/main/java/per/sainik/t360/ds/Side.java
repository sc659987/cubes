package per.sainik.t360.ds;

/***
 * different side of {@link Cube} & {@link SquareShape}
 */
public enum Side {

  /***
   * Bottom Side of {@link Cube}
   */
  BOTTOM,
  /***
   * Up Side of {@link Cube} or {@link SquareShape}
   */
  UP,
  /***
   * Right side of {@link Cube} or {@link SquareShape}
   */
  RIGHT,
  /***
   * down side of {@link Cube} or {@link SquareShape}
   */
  DOWN,
  /***
   * left side of {@link Cube} or {@link SquareShape}
   */
  LEFT,
  /***
   * Top Side of {@link Cube} or {@link SquareShape}
   */
  TOP,
  /***
   * It's used by {@link Cube} for logic
   */
  NONE

}


